        <h2>Instagram Percepat Rilis Fitur Co-Watching</h2>
    	<img src="gambar2.jpg">
        <p>Jakarta, Selular.ID – Instagram memperkenalkan fitur barunya yang diberi nama Co-watching, dimana teman pada video chat bisa melihat postingan yang telah di-like maupun yang disimpan. Dalam blog resminya, Instagram mengatakan bahwa peluncuran Co-watching dipercepat seiring diterapkannya social distancing di banyak negara. Untuk menggunakan Co-Watching, pengguna bisa membuka Instagram Direct, ketuk icon pesawat kertas di sudut kanan atas aplikasi. Kemudian mulai obrolan video baru dengan mencari atau memilih kontak di kotak masuk. Lalu, ketuk icon kamera pada halaman pesan direct ini. Setelah mulai mengobrol, cukup ketuk ikon foto di bagian bawah panel untuk melihat yang disukai, disimpan, dan rekomendasi Instagram yang tersedia. Sebelumnya, pengujian fitur Co-watching Instagram ini pertama kali diungkap pada April 2019 oleh Jane Manchun Wong, peneliti Instagram. Namun saat itu, ia belum bisa memastikan kapan peluncuran fitur ini. Saat ini, fitur Co-watching sudah tersedia untuk pengguna global, kecuali di Amerika Serikat. Fitur Co-watching dapat diakses oleh siapa saja yang berpartisipasi dalam video chat di Instagram.<p>
