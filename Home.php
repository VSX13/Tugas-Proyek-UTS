    	<h2>Apple Akan Kenalkan Headset AR di 2022</h2>
    	<img src="gambar1.jpg">
        <p>Jakarta, Selular.ID – Apple akan mengenalkan headset augmented reality (AR) pada 2022.
        	Seperti dikutip MacRumours, menurut Ibtimes, headset augmented reality Apple sudah berlangsung, dan mungkin akan dirilis dalam waktu beberapa tahun. Headset ini, sesuai iPhoneHacks, akan terlihat mirip dengan Oculus Quest. Ini berarti bukan jenis yang akan dipakai orang di jalanan, tetapi itu akan memungkinkan pengguna untuk menikmati ARKit Apple dan fitur augmented reality lainnya.Hal ini sudah menjadi perbincangan sejak tahun lalu, melansir The Information, Apple sedang menyiapkan kacamata dengan AR untuk beberapa tahun ke depan. Pejabat Apple membicarakan rentang waktu untuk kedua produk tersebut pada Oktober 2019 dalam rapat internal yang dipimpin oleh Mike Rockwell, Wakil Direktur, mengenai kehadiran perangkat tersebut.Dan sejauh ini Apple belum berkomentar mengenai isu tersebut.Teknologi AR sedang menjadi primadona di kalangan raksasa teknologi, Google, Microsoft dan Facebook berlomba-lomba mengaplikasikan teknologi tersebut pada produk mereka.<p>
        
